// Hook to use translations
export const useTranslations = (language = 'ar') => {
  const t = (key) => {
    const keys = key.split('.');
    let value = translations[language];
    
    for (const k of keys) {
      value = value?.[k];
      if (!value) return key; // Return key if translation not found
    }
    
    return value;
  };

  return { t };
};

