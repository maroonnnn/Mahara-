// Helper function to get category by slug
export const getCategoryBySlug = (slug) => {
  return categories.find(cat => cat.slug === slug);
};

// Helper function to get all subcategories from a category
export const getSubcategoriesFromCategory = (category) => {
  if (!category) return [];
  return category.subCategories || [];
};

// Helper function to get subcategory by slugs
export const getSubcategoryBySlug = (categorySlug, subcategorySlug) => {
  const category = getCategoryBySlug(categorySlug);
  if (!category) return null;
  
  const subcategories = getSubcategoriesFromCategory(category);
  return subcategories.find(sub => sub.slug === subcategorySlug);
};

